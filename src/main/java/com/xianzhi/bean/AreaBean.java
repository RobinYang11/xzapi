package com.xianzhi.bean;

public class AreaBean {

	private int AreaId;
	private String AreaName;
	private String AreaAddTime;
	private String AreaLastEditTime;

	public int getAreaId() {
		return AreaId;
	}

	public void setAreaId(int areaId) {
		AreaId = areaId;
	}

	public String getAreaName() {
		return AreaName;
	}

	public void setAreaName(String areaName) {
		AreaName = areaName;
	}

	public String getAreaAddTime() {
		return AreaAddTime;
	}

	public void setAreaAddTime(String areaAddTime) {
		AreaAddTime = areaAddTime;
	}

	public String getAreaLastEditTime() {
		return AreaLastEditTime;
	}

	public void setAreaLastEditTime(String areaLastEditTime) {
		AreaLastEditTime = areaLastEditTime;
	}

}
