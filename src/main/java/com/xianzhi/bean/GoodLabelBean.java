package com.xianzhi.bean;

public class GoodLabelBean {

	private int id;
	private String goodLabelName;
	private int priority;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGoodLabelName() {
		return goodLabelName;
	}

	public void setGoodLabelName(String goodLabelName) {
		this.goodLabelName = goodLabelName;
	}

	public int getPriority() {
		return priority;
	}

	public void setPriority(int priority) {
		this.priority = priority;
	}

}
