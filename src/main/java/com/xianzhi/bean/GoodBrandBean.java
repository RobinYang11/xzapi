package com.xianzhi.bean;

public class GoodBrandBean {

	private int id;
	private String goodBrandName;
	private String goodBrandLogo;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGoodBrandName() {
		return goodBrandName;
	}

	public void setGoodBrandName(String goodBrandName) {
		this.goodBrandName = goodBrandName;
	}

	public String getGoodBrandLogo() {
		return goodBrandLogo;
	}

	public void setGoodBrandLogo(String goodBrandLogo) {
		this.goodBrandLogo = goodBrandLogo;
	}

}
