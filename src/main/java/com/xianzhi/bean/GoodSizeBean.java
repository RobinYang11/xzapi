package com.xianzhi.bean;

public class GoodSizeBean {

	private int id;
	private int GoodId;
	private String goodSize;
	private double goodSizePrice;
	private String goodSizeBak;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getGoodId() {
		return GoodId;
	}

	public void setGoodId(int goodId) {
		GoodId = goodId;
	}

	public String getGoodSize() {
		return goodSize;
	}

	public void setGoodSize(String goodSize) {
		this.goodSize = goodSize;
	}

	public double getGoodSizePrice() {
		return goodSizePrice;
	}

	public void setGoodSizePrice(double goodSizePrice) {
		this.goodSizePrice = goodSizePrice;
	}

	public String getGoodSizeBak() {
		return goodSizeBak;
	}

	public void setGoodSizeBak(String goodSizeBak) {
		this.goodSizeBak = goodSizeBak;
	}

}
